#include <stdio.h>
#include <stdlib.h>

/** \brief Función sumar para dos operandos
 *
 * \param Operando n° 1 ingresado por el usuario
 * \param Operando n° 2 ingresado por el usuario
 * \return Devuelve el resultado de la suma
 *
 */
float suma(float n1, float n2);

/** \brief Función restar para dos operandos
 *
 * \param Operando n° 1 ingresado por el usuario
 * \param Operando n° 2 ingresado por el usuario
 * \return Devuelve el resultado de la resta
 *
 */
float resta(float n1, float n2);

/** \brief Función dividir para dos operandos
 *
 * \param Operando n° 1 ingresado por el usuario
 * \param Operando n° 2 ingresado por el usuario
 * \return Devuelve el resultado de la division
 *
 */

float division(float n1, float n2);

/** \brief Función multiplicar para dos operandos
 *
 * \param Operando n° 1 ingresado por el usuario
 * \param Operando n° 2 ingresado por el usuario
 * \return Devuelve el resultado de la multiplicacion
 *
 */
float multip(float n1, float n2);

/** \brief El factorial de un numero, es el producto de todos los numeros enteros positivos desde 1 hasta el ingresado por el usuario
 *
 * \param Operando n° 1 ingresado por el usuario
 * \return Devuelve el resultado de la factorizacion
 *
 */
float factor(float n1);


